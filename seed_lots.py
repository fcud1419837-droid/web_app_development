def seed():
    os.makedirs('instance', exist_ok=True)
    conn = sqlite3.connect(DB_PATH)
    conn.row_factory = sqlite3.Row

    # 若已有資料則跳過
    count = conn.execute("SELECT COUNT(*) FROM lots").fetchone()[0]
    if count > 0:
        print(f'籤詩資料已存在（共 {count} 筆），略過 seed。')
        conn.close()
        return

    conn.executemany(
        "INSERT INTO lots (lot_number, type, poem, explanation) VALUES (?, ?, ?, ?)",
        LOTS
    )
    conn.commit()
    print(f'已成功寫入 {len(LOTS)} 筆籤詩資料。')
    conn.close()

if __name__ == '__main__':
    seed()
